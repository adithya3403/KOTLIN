fun main() {
    // output can be done using println()
    println("Hello World");
    // println() automatically adds a newline "\n" at the end
    // print() can be used to output without newline
    print("On the ");
    print("same line");
    // example
    var num : Int = 42;
    println("Num is " + num);
    println("Num is $num");
    // example
    var a : Int = 42;
    var b : Int = 8;
    println("Result : ${a+b}");
}